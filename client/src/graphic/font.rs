/// Draws a text string at (x, y) with the given scale and color.
/// It uses a horizontal grouping algorithm to drastically reduce OpenGL clear calls.
pub unsafe fn draw_text(
    renderer: &mut Renderer,
    text: &str,
    start_x: i32,
    start_y: i32,
    r: f32,
    g: f32,
    b: f32,
    a: f32,
    scale: i32,
) {
    renderer.set_color(r, g, b, a);

    let char_width = 5 * scale;
    let char_space = scale;

    let mut current_x = start_x;

    for ch in text.chars() {
        let ch_num = ch as usize;
        if ch_num >= 32 && ch_num <= 126 {
            let glyph = &FONT_5X7[ch_num - 32];

            // Render each row grouping contiguous pixels horizontally
            for row in 0..8 {
                let mut col = 0;
                while col < 5 {
                    // Check if current pixel is on
                    if (glyph[col] & (1 << row)) != 0 {
                        // Find how many contiguous pixels are on in this row
                        let mut width = 1;
                        while col + width < 5 && (glyph[col + width] & (1 << row)) != 0 {
                            width += 1;
                        }

                        // Dispatch a unified block for grouped pixels
                        renderer.draw_rect(
                            current_x + (col as i32 * scale),
                            start_y + (row as i32 * scale),
                            (width as i32) * scale,
                            scale,
                        );

                        // Skip the pixels we just grouped
                        col += width;
                    } else {
                        col += 1;
                    }
                }
            }
        }
        current_x += char_width + char_space;
    }
}

/// Helper function to calculate the total width of a string in pixels at a given scale.
pub fn get_text_width(text: &str, scale: i32) -> i32 {
    let char_width = 5 * scale;
    let char_space = scale;

    let valid_chars = text
        .chars()
        .filter(|ch| {
            let n = *ch as usize;
            n >= 32 && n <= 126
        })
        .count() as i32;

    if valid_chars == 0 {
        return 0;
    }

    // Include space between characters, but not after the last one
    (valid_chars * char_width) + ((valid_chars - 1) * char_space)
}
